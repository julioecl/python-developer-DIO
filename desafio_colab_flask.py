import pandas as pd
from flask_ngrok import run_with_ngrok
from flask import request, jsonify, Flask
import random as rk

app = Flask(__name__)
run_with_ngrok(app)

d = {
    "name": "Nikola",
    "surname": "Tesla",
    "age": 60
}

@app.route('/')

def home():
  return "Sa√≠da gerada"

@app.route('/input')

def input():
  return jsonify(d)

@app.route('/output', methods=['GET, POST'])

def predJson():
  pred = rk.choice(['positive','negative'])
  nd = d
  nd['prediction']=pred
  return jsonify(nd)

app.run()